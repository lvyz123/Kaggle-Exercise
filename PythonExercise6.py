from learntools.python import roulette
import random

def random_and_superstitious(wheel):
    """Interact with the given wheel over 100 spins with the following strategy:
    - if the wheel lands on 4, don't bet on the next spin
    - otherwise, bet on a random number on the wheel (from 0 to 10)
    """
    last_number = 0
    while wheel.num_remaining_spins() > 0:
        if last_number == 4:
            # Unlucky! Don't bet anything.
            guess = None
        else:
            guess = random.randint(0, 10)
        last_number = wheel.spin(number_to_bet_on=guess)

roulette.evaluate_roulette_strategy(random_and_superstitious)

def my_agent(wheel):
    """Interact with the given wheel over 100 spins with the following strategy:
    - if the wheel lands on 4, don't bet on the next spin
    - otherwise, bet on a random number on the wheel (from 0 to 10)
    """
    last_number = 0
    num_rec={}
    import numpy as np
    while wheel.num_remaining_spins() > 0:
        if last_number == 4 and '4' not in num_rec.keys():
            # Unlucky! Don't bet anything.
            guess = None
            last_number = wheel.spin(number_to_bet_on=guess)
            num_rec['4']=[last_number]
        elif last_number == 4 and num_rec['4']:
            a = np.array(num_rec['4'])
            counts = np.bincount(a)
            guess = np.argmax(counts)
            last_number = wheel.spin(number_to_bet_on=guess)
            num_rec['4'].append(last_number)
        elif str(last_number) not in num_rec.keys():
            guess = None
            last_number = wheel.spin(number_to_bet_on=guess)
            num_rec[int(last_number)]=[last_number]
        else:
            dist_mean = np.mean(num_rec[str(last_number)])
            guess = round(dist_mean)
            last_number = wheel.spin(number_to_bet_on=guess)
            num_rec[int(last_number)].append(last_number)

roulette.evaluate_roulette_strategy(my_agent)
